     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

         $location = $request->input('location');
         $city = $request->input('city');
         $cityLat = $request->input('cityLat');
         $cityLng = $request->input('cityLng');
         $month = $request->input('month');

         DB::insert('insert into form (location, city, cityLat, cityLng, month) values (?, ?, ?, ?, ?)', [$location, $city, $cityLat, $cityLng, $month]);
        
        return redirect()->back()->with('message', 'Data Stored Successfully!');
    }

